# 무관 성별 처리를 위한 기본값
GENDER_DEFAULT = {
    "남": "남",
    "여": "여",
    "무관": "남",
}


def get_outfit(schedule: str, style: str, gender: str) -> dict:
    g = GENDER_DEFAULT.get(gender, "남")
    key = (schedule, style, g)
    if key in OUTFIT_DB:
        return OUTFIT_DB[key]
    # fallback: 스타일만 맞는 것
    for k, v in OUTFIT_DB.items():
        if k[0] == schedule and k[2] == g:
            return v
    # 최종 fallback
    return OUTFIT_DB[("학교", "캐주얼", "남")]


@app.get("/")
def root():
    return {"message": "오늘 뭐 입지? 코디 추천 API"}


@app.post("/recommend", response_model=OutfitRecommendation)
async def recommend(req: RecommendRequest):
    # 1. 날씨 정보 가져오기
    async with httpx.AsyncClient() as client:
        try:
            resp = await client.get(
                WEATHER_URL,
                params={
                    "q": req.city,
                    "appid": WEATHER_API_KEY,
                    "units": "metric",
                    "lang": "kr",
                },
                timeout=10,
            )
            if resp.status_code != 200:
                raise HTTPException(status_code=400, detail=f"도시 '{req.city}'의 날씨 정보를 찾을 수 없어요. 영문 도시명을 입력해주세요. (예: Seoul, Busan)")
            weather_data = resp.json()
        except httpx.TimeoutException:
            raise HTTPException(status_code=504, detail="날씨 API 응답 시간이 초과됐어요. 잠시 후 다시 시도해주세요.")

    temp = weather_data["main"]["temp"]
    weather_main = weather_data["weather"][0]["main"]
    weather_desc = weather_data["weather"][0]["description"]
    weather_icon = weather_data["weather"][0]["icon"]

    # 2. 코디 추천 로직
    outfit = get_outfit(req.schedule, req.style, req.gender)
    outer = get_outer(temp, weather_main)

    return OutfitRecommendation(
        city=weather_data["name"],
        temperature=round(temp, 1),
        weather_desc=weather_desc,
        weather_icon=weather_icon,
        concept=outfit["concept"],
        concept_desc=outfit["concept_desc"],
        top=outfit["top"],
        bottom=outfit["bottom"],
        outer=outer,
        accessory=outfit["accessory"],
        style_tip=outfit["style_tip"],
    )
