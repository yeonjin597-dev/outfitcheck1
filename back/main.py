def get_outer(temp: float, weather_main: str) -> str | None:
    if weather_main in ["Rain", "Drizzle", "Thunderstorm"]:
        return "☂️ 우산 또는 방수 재킷 필수"
    if temp < 5:
        return "🧥 두꺼운 패딩 또는 코트"
    elif temp < 12:
        return "🧣 가디건 + 머플러 또는 가벼운 재킷"
    elif temp < 18:
        return "🧥 얇은 재킷 또는 가디건"
    elif temp < 23:
        return "👕 아우터 없이도 괜찮아요 (저녁엔 얇은 겉옷 추천)"
    else:
        return None


